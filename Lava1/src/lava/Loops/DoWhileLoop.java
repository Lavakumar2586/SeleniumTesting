package lava.Loops;

public class DoWhileLoop {

	public static void main(String[] args) {

/*       do While Loop ==> syntax:-  ==>>
   *
   *    Initialization;
   *    do{
   *       //Statement / Statements
   *       increment / decrement
   *    }
   *    while (Condition);		*/
		
		int a=10;
		do {
			System.out.println(a +"     "+"Lava");
		a++;
		} while (a<15);
		System.out.println("**************");
	//****************************************************
		int b=5;
		do {
			System.out.println(b+"    "+ "Webdriver");
			b=b+1;
		}
		while (b<=14);
		
		
		}

}
